from pathlib import Path

import pandas as pd
import plotly.express as px
import streamlit as st


PROJECT_ROOT = Path(__file__).resolve().parents[2]
RESULTS_ROOT = PROJECT_ROOT / "dashboard" / "data"

BRAND_RESULTS_PATH = RESULTS_ROOT / "brand_results.csv"
TOPIC_RESULTS_PATH = RESULTS_ROOT / "topic_results.csv"

SKY = "#68B6D9"
NAVY = "#17324D"
GRID = "#DCE8EF"


def _style_horizontal_chart(
    figure,
    *,
    x_title: str,
    height: int,
):
    figure.update_traces(
        marker_color=SKY,
        marker_line_width=0,
        textposition="outside",
        cliponaxis=False,
        hovertemplate="%{y}<br>%{x}<extra></extra>",
    )

    figure.update_layout(
        height=height,
        margin=dict(l=8, r=48, t=8, b=42),
        paper_bgcolor="rgba(0,0,0,0)",
        plot_bgcolor="rgba(0,0,0,0)",
        showlegend=False,
        font=dict(color=NAVY),
    )

    figure.update_xaxes(
        title=x_title,
        showgrid=True,
        gridcolor=GRID,
        zeroline=False,
    )

    figure.update_yaxes(
        title=None,
        showgrid=False,
    )

    return figure


def render_model_results() -> None:
    st.divider()
    st.header("Latest model results")
    st.caption(
        "Generated by the latest sentiment and topic-model pipeline."
    )

    if not BRAND_RESULTS_PATH.exists():
        st.warning(f"Missing brand results: {BRAND_RESULTS_PATH}")
        return

    if not TOPIC_RESULTS_PATH.exists():
        st.warning(f"Missing topic results: {TOPIC_RESULTS_PATH}")
        return

    brand_results = pd.read_csv(BRAND_RESULTS_PATH)
    topic_results = pd.read_csv(TOPIC_RESULTS_PATH)

    if brand_results.empty or topic_results.empty:
        st.warning("The latest model-result files are empty.")
        return

    brand_sector, topic_sector = st.columns(
        [1, 1.15],
        gap="large",
    )

    with brand_sector:
        with st.container(border=True):
            st.subheader("Brand sentiment comparison")
            st.caption("Positive sentiment by brand")

            brand_columns = st.columns(len(brand_results))

            for column, row in zip(
                brand_columns,
                brand_results.itertuples(index=False),
            ):
                column.metric(
                    label=str(row.brand),
                    value=f"{float(row.pct_positive):.1f}%",
                    delta=f"{int(row.n):,} reviews",
                )

            brand_chart = brand_results.sort_values(
                "pct_positive",
                ascending=True,
            )

            brand_figure = px.bar(
                brand_chart,
                x="pct_positive",
                y="brand",
                orientation="h",
                text="pct_positive",
            )

            brand_figure.update_traces(
                texttemplate="%{x:.1f}%",
            )

            brand_figure = _style_horizontal_chart(
                brand_figure,
                x_title="Positive sentiment",
                height=350,
            )

            brand_figure.update_xaxes(
                range=[0, 105],
                ticksuffix="%",
            )

            st.plotly_chart(
                brand_figure,
                use_container_width=True,
                config={"displayModeBar": False},
            )

    with topic_sector:
        with st.container(border=True):
            st.subheader("Top conversation topics")
            st.caption("Topics ranked by number of mentions")

            ranked_topics = (
                topic_results
                .sort_values("count", ascending=False)
                .reset_index(drop=True)
            )

            top_topics = (
                ranked_topics
                .head(10)
                .sort_values("count", ascending=True)
            )

            topic_figure = px.bar(
                top_topics,
                x="count",
                y="topic",
                orientation="h",
                text="count",
            )

            topic_figure.update_traces(
                texttemplate="%{x:,.0f}",
            )

            topic_figure = _style_horizontal_chart(
                topic_figure,
                x_title="Mentions",
                height=500,
            )

            st.plotly_chart(
                topic_figure,
                use_container_width=True,
                config={"displayModeBar": False},
            )

    with st.container(border=True):
        st.subheader("Topic details")

        ranked_topics = (
            topic_results
            .sort_values("count", ascending=False)
            .reset_index(drop=True)
        )

        st.dataframe(
            ranked_topics,
            use_container_width=True,
            hide_index=True,
            column_config={
                "topic": st.column_config.TextColumn(
                    "Topic",
                    width="medium",
                ),
                "count": st.column_config.NumberColumn(
                    "Mentions",
                    format="%d",
                ),
                "example": st.column_config.TextColumn(
                    "Example",
                    width="large",
                ),
            },
        )

    latest_timestamp = max(
        BRAND_RESULTS_PATH.stat().st_mtime,
        TOPIC_RESULTS_PATH.stat().st_mtime,
    )

    st.caption(
        "Results refresh automatically when this page reruns. "
        f"Latest file timestamp: "
        f"{pd.Timestamp(latest_timestamp, unit='s'):%Y-%m-%d %H:%M}"
    )
